/**
 * raycaster - Implement raycasting for each frame
 * @sdl: SDL instance
 * @rc: Raycasting instance
 */
void raycaster(t_sdl *sdl, t_raycaster *rc)
{
	int done = 0;
	int x;

	while (done != 1)
	{
		for (x = 0; x < WIN_X; x++)
		{
			initial_calc(rc, x);
			perform_dda(rc);
			calc_wall_height(rc);
			draw_vert_line(sdl, rc, x);
		}
		render_frame(sdl);
		if (handle_events(rc) != 0)
			done = 1;
	}
}


/**
 * main - Entry point
 * Return: Always 0
 */
int main(void)
{
	t_sdl sdl;
	t_raycaster rc;

	/* Initialization */
	if (init(&sdl, &rc) != 0)
		return (-1);
	/* Raycasting */
	raycaster(&sdl, &rc);
	/* Close SDL */
	if (sdl.renderer)
		SDL_DestroyRenderer(sdl.renderer);
	if (sdl.window)
		SDL_DestroyWindow(sdl.window);
	SDL_Quit();
	return (0);
}
